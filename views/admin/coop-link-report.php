<p>Download a CSV of all links found on pages, posts, highlights, or in Menus.</p>
<p>The first column is the link, the second column is a list of URLs of the pages where the link was found, or the name of the menu item.</p>
<p><em>Note that some links may appear twice if they use an <code>https</code> protocol in some places but an <code>http</code> protocol in others.</em></p>
